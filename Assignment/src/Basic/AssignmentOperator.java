package Basic;
public class AssignmentOperator {
	
	public static void main(String args[]) {
		int a=2345;
		System.out.println("a= :"+a);
		a+=8;
		System.out.println("a+=2 :"+a);
		a*=5;
		System.out.println("a*=4 :"+a);
		a/=3;
		System.out.println("a/=5 :"+a);
		a%=5;
		System.out.println("a%=6 :"+a);
	}
}

