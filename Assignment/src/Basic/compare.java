package Basic;
public class compare {
	public static void main(String args[]) {
		int a=90;
		int b=85;
		System.out.println("a= :"+a);
		System.out.println("b=  :"+b);
		System.out.println("a==b :"+(a==b));
		System.out.println("a!=b :"+(a!=b));
		System.out.println("a<=b :"+(a<=b));
		System.out.println("a>=b  :"+(a>=b));
		System.out.println("a<b :"+(a<b));
		System.out.println("a>b  :"+(a>b));	
	}
}
